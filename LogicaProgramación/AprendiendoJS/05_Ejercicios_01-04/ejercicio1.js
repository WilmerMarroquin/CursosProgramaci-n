/* 
Ejercicio #1

Calcular cuántas horas le llevaría llegar a la luna a una nave espacial y guarda el resultado en una variable.

La distancia de la tierra a la luna es de 384400 kilómetros

La velocidad de la nave es de 1225 kilómetros por hora.
*/

let distanciaTierraLuna = 384400;
let velocidadNave = 1225;

let tiempoTierraLuna = distanciaTierraLuna / velocidadNave;

console.log(`Necesitaría ${tiempoTierraLuna} horas para llegar a la luna.`);

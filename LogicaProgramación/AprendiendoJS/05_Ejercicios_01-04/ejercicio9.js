/*
Ejercicio #9

Una milla son 1.6 kilómetros, cuantos kilómetros tiene una autopista de 120 millas?
*/

let milla = 1.6; // Kilómetros por milla
let autopista = 120; // Millas

// Conversión de millas a kilómetros

let kilómetrosAutopista = autopista * milla;

console.log("La autopista tiene", kilómetrosAutopista, "kilómetros.");
/*
Ejercicio #5

Tengo uan tarta de queso con 16 porciones, cada porción vale 3.8 dolares.

¿Cuánto dinero tendría si vendo todas las porciones?
*/

let porciones = 16;
let valorPorción = 3.8;

let totalDinero = porciones * valorPorción;

console.log(`Si vendo todas las porciones de la tarta de queso, tendría ${totalDinero} dolares.`);
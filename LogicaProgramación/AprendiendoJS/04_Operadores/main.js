// Operadores aritméticos
let num1 =5;
let num2 = 2;

let suma = num1 + num2;
let resta = num1 - num2;
let multiplicación = num1 * num2;
let division = num1 / num2;
let modulo = num1 % num2;
let potencia = num1 **2;

console.log("Numero 1: " + num1);
console.log("Numero 2: " + num2);

console.log("Suma: ", suma);
console.log("Resta: ", resta);
console.log("Multiplicación: ", multiplicación);
console.log("División: ", division);
console.log("Módulo: ", modulo);
console.log("Potencia: ", potencia);

// Operadores de asignación
let numerito= 5
console.log("Numerito: " + numerito);

numerito += 2; // numerito = numerito + 2
console.log("numerito + 2: " + numerito);
numerito -= 3; // numerito = numerito - 2
console.log("numerito - 3: " + numerito);
numerito *= 3; // numerito = numerito * 3
console.log("numerito * 3: " + numerito);
numerito /= 2; // numerito = numerito / 2
console.log("numerito / 2: " + numerito);

// Operadores de comparación

console.log("5 > 3: ", 5 > 3);
console.log("5 < 3: ", 5 < 3);
console.log("5 >= 3: ", 5 >= 3);
console.log("5 <= 3: ", 5 <= 3);
console.log("5 == 5: ", 5 == 5);
console.log("5!= 5: ", 5!= 5);
console.log("5 === '5': ", 5 === "5");


// Operadores lógicos

console.log("true && true: ", true && true);
console.log("true && false: ", true && false);
console.log("false && true: ", false && true);
console.log("false && false: ", false && false);
console.log("true || true: ", true || true);
console.log("true || false: ", true || false);
console.log("false || true: ", false || true);
console.log("false || false: ", false || false);
console.log("!true: ",!true);
console.log("!false: ",!false);


// Operadores de cadena

let palabra = "Hola";
console.log("Palabra: ", palabra);

palabra += " mundo";
console.log("Palabra concatenada: ", palabra);

// Operadores de incremento y decremento

let contador = 0;
console.log("Contador: ", contador);

contador++; // contador = contador + 1
console.log("Contador incrementado: ", contador);

contador--; // contador = contador - 1
console.log("Contador decrementado: ", contador);
